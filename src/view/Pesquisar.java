    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        String matricula = txtMat.getText().trim();
         
        if (matricula.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Informe a matrícula para remover.", "Aviso", JOptionPane.WARNING_MESSAGE);
        return;
    }
        Aluno alunoEncontrado = controller.buscarAlunoPorMatricula(matricula);

    if (alunoEncontrado != null) {
        controller.removerAluno(matricula);
        JOptionPane.showMessageDialog(this, "Aluno removido com sucesso.");
    } else {
        JOptionPane.showMessageDialog(this, "Aluno não encontrado.", "Aviso",JOptionPane.WARNING_MESSAGE);
    }
  
    }//GEN-LAST:event_btnRemoverActionPerformed

