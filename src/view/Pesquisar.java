    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
       String matricula = txtMat.getText().trim();
   
        try {
        Aluno aluno = controller.buscarAlunoPorMatricula(matricula);
        
        if (aluno != null) {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

            String mensagem = String.format(
                "Matrícula: %s\nNome: %s\nIdade: %d\nData Nasc.: %s\nTelefone: %s\nCPF: %s",
                aluno.getMatricula(),
                aluno.getNome(),
                aluno.getIdade(),
                sdf.format(aluno.getDataNasc()),
                aluno.getTelefone(),
                aluno.getCpf()
            );

            JOptionPane.showMessageDialog(this, mensagem, "Aluno encontrado", JOptionPane.INFORMATION_MESSAGE);

        } else {
            JOptionPane.showMessageDialog(this, "Aluno não encontrado.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao buscar: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnPesquisarActionPerformed

